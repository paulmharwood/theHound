package com.fujitsu.uk.markdown;

public class ParametersRequired {

    boolean includeDescription = false;
    boolean includeAllowedPattern = false;
    boolean includeAllowedValues = false;
    boolean includeConstraintDescription = false;
    boolean includeDefault = false;
    boolean includeMinLength = false;
    boolean includeMaxLength = false;
    boolean includeMinValue = false;
    boolean includeMaxValue = false;
    boolean includeNoEcho = false;

    public boolean isIncludeDescription() {
        return includeDescription;
    }

    public void setIncludeDescription(boolean includeDescription) {
        this.includeDescription = includeDescription;
    }

    public boolean isIncludeAllowedPattern() {
        return includeAllowedPattern;
    }

    public void setIncludeAllowedPattern(boolean includeAllowedPattern) {
        this.includeAllowedPattern = includeAllowedPattern;
    }

    public boolean isIncludeAllowedValues() {
        return includeAllowedValues;
    }

    public void setIncludeAllowedValues(boolean includeAllowedValues) {
        this.includeAllowedValues = includeAllowedValues;
    }

    public boolean isIncludeConstraintDescription() {
        return includeConstraintDescription;
    }

    public void setIncludeConstraintDescription(boolean includeConstraintDescription) {
        this.includeConstraintDescription = includeConstraintDescription;
    }

    public boolean isIncludeDefault() {
        return includeDefault;
    }

    public void setIncludeDefault(boolean includeDefault) {
        this.includeDefault = includeDefault;
    }

    public boolean isIncludeMinLength() {
        return includeMinLength;
    }

    public void setIncludeMinLength(boolean includeMinLength) {
        this.includeMinLength = includeMinLength;
    }

    public boolean isIncludeMaxLength() {
        return includeMaxLength;
    }

    public void setIncludeMaxLength(boolean includeMaxLength) {
        this.includeMaxLength = includeMaxLength;
    }

    public boolean isIncludeMinValue() {
        return includeMinValue;
    }

    public void setIncludeMinValue(boolean includeMinValue) {
        this.includeMinValue = includeMinValue;
    }

    public boolean isIncludeMaxValue() {
        return includeMaxValue;
    }

    public void setIncludeMaxValue(boolean includeMaxValue) {
        this.includeMaxValue = includeMaxValue;
    }

    public boolean isIncludeNoEcho() {
        return includeNoEcho;
    }

    public void setIncludeNoEcho(boolean includeNoEcho) {
        this.includeNoEcho = includeNoEcho;
    }
}
